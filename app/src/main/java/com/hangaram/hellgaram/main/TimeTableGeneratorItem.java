package com.hangaram.hellgaram.main;

public class TimeTableGeneratorItem {
    private String subjectWithClassIdentifier;
    private String teacher;
    private String room;

    public String getSubjectWithClassIdentifier() {
        return subjectWithClassIdentifier;
    }

    public void setSubjectWithClassIdentifier(String subjectWithClassIdentifier) {
        this.subjectWithClassIdentifier = subjectWithClassIdentifier;
    }

    public String getTeacher() {
        return teacher;
    }

    public void setTeacher(String teacher) {
        this.teacher = teacher;
    }

    public String getRoom() {
        return room;
    }

    public void setRoom(String room) {
        this.room = room;
    }
}
